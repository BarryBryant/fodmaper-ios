import Foundation
import UIKit

extension UIColor {

    class func fodmaperPurple() -> UIColor {
        return UIColor(red: 196 / 255, green: 127 / 255, blue: 218 / 255, alpha: 1.0)
    }
    
    class func fodmaperGreen() -> UIColor {
        return UIColor(red: 178 / 255, green: 221 / 255, blue: 120 / 255, alpha: 1.0)
    }
    
    class func fodmaperOrange() -> UIColor {
        return UIColor(red: 243 / 255, green: 187 / 255, blue: 114 / 255, alpha: 1.0)
    }
    
    class func fodmaperRed() -> UIColor {
        return UIColor(red: 255 / 255, green: 132 / 255, blue: 167 / 255, alpha: 1.0)
    }
    
}
